    private void txtBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyPressed
        String [] titulos = {"ID","Nombre y Apellido","Telefono","Fecha de Vencimiento","Compañia"};
        String registros []= new String [50];
        String sql="select * from clientes WHERE nombre_apellido LIKE '%"+txtBuscar.getText()+"%'";
        
        model = new DefaultTableModel(null,titulos);
        
        try {
            Statement st = (Statement) cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                registros[0]=rs.getString("ID");
                registros[1]=rs.getString("nombre_apellido");
                registros[2]=rs.getString("numero_telefono");
                registros[3]=rs.getString("fecha_vencimiento");
                registros[4]=rs.getString("compañia");
                model.addRow(registros);
            }
           jTableDatosClientes.setModel(model);
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al buscar: "+e);
        }
    }//GEN-LAST:event_txtBuscarKeyPressed

