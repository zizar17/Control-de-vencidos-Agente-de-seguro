    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
       try {
          PreparedStatement ps = cn.prepareStatement("INSERT INTO clientes (nombre_apellido,numero_telefono,fecha_vencimiento,compañia) VALUES (?,?,?,?)");
          ps.setString(1, txtNombre.getText());
          ps.setString(2, txtTel.getText());
          ps.setString(3, txtFecha.getText());
          ps.setString(4, boxCompañia.getSelectedItem().toString());
          ps.executeUpdate();
          JOptionPane.showMessageDialog(null,"DATOS GUARDADOS CORRECTAMENTE");
          mostrardatos();
          limpiarentradas();
       } catch(SQLException e){
           JOptionPane.showMessageDialog(null, "No se cargo ningun dato: ");
           System.out.println("Error: "+e.getMessage());
       }
       txtNombre.requestFocus();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
      try{
          PreparedStatement ps= cn.prepareStatement("Update clientes set nombre_apellido='"+txtNombre.getText()+"',numero_telefono='"+txtTel.getText()+"',fecha_vencimiento='"+txtFecha.getText()+"',compañia='"+boxCompañia.getSelectedItem()+"' where id='"+txtId.getText()+"'");
           int indice = ps.executeUpdate();
           if (indice>0){
               JOptionPane.showMessageDialog(rootPane, "DATOS ACTUALIZADOS CORRECTAMENTE");
               mostrardatos();
               limpiarentradas();
           }
           else{
             JOptionPane.showMessageDialog(null, "No se selecciono niguna fila");
           }
      }catch(SQLException e){
          JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR DATOS");
          JOptionPane.showMessageDialog(null, "ERROR: "+e.getMessage());
      }
    }//GEN-LAST:event_btnActualizarActionPerformed

